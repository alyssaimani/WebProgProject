    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::find($id);
        $roles = ['admin', 'customer'];
        
        return view('account/edit', [
            'user' => $user,
            'title' => 'Update Account',
            'roles' => $roles
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validatedData = $request->validate([
            'name' => ['required', Rule::unique('users')->ignore($id)],
            'email' => ['required', 'email:dns', Rule::unique('users')->ignore($id)],
            'password' => 'required|min:8|max:20',
            'role' => 'required|in:admin,customer'
        ]);
        User::where('id', $id)
        ->update($validatedData);
        return redirect('account')->with('success', 'Account has been updated!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        User::destroy($id);
        return redirect('account')->with('success', 'Account has been deleted!');
    }
